using System;
class FloatOrDouble
{
    static void Main()
    {
        double d = 8923.1234857d;
        float f = 12.345f;
        double d2 = 3456.091d;
        double d3 = 34.567839023d;
      
        Console.WriteLine("D is {0}", d);
        Console.WriteLine("F is {0}", f);
        Console.WriteLine("D2 is {0}", d2);
        Console.WriteLine("D3 is {0}", d3);
    }
}